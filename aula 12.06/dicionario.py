# dicionario - é uma coleção de itens que possuem chave e valor
# chave:valor
# parametro: {}
# inicializo: dic = {} ou dic = dict(chave = valor)
# clear - limpa o dicionario
# del - exclui uma chave del.dic[chave]
# keys - ele vai entregar todas as chaves do dicionario
# values - ele vai entregar todos os valores
# items - ele entrega todos os itens com chave e valor
# get - ele entrega um valor referenciado pela chave
# update - ele é utilizado para inserir itens no dicionario
# copy - ele faz uma copia do dicionario garantindo um novo endereço de memoria
# sorted - ordena um dicionario pelas as suas chaves

